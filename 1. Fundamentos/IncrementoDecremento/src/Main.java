public class Main {

    public static void main(String[] args) {
        int totalDownloads = 10;

        //Incrementos pré-fixado
        ++totalDownloads;

        //Incremento pós-fixado
        totalDownloads++;

        //Decremento pré-fixado
        --totalDownloads;

        //Decremento pós-fixados
        totalDownloads--;

        System.out.println("Total de Downloads: " + totalDownloads);
    }
}